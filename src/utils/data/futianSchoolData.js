// 查询学校，如果不传名字，则返回全部
export default function getSchools(name) {
  if (!name) {
    return [...data]
  }
  const nameArr = unicodeToArr(name)
  return dataSplit
    .filter(d => !nameArr.some(n => !d.includes(n)))
    .map(d => d.join(''))
}

function unicodeToArr(str) {
  const arr = []
  for (let i of str) {
    arr.push(i)
  }
  return arr
}
